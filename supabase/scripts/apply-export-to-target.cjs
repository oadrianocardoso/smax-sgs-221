const fs = require('node:fs/promises');
const path = require('node:path');
const { Client } = require('pg');

const ROOT = process.cwd();
const MIGRATIONS_DIR = path.join(ROOT, 'supabase', 'migrations');
const EXPORT_BASE_DIR = path.join(ROOT, 'supabase', 'export');

const IMPORT_ORDER = [
  'smax_teams',
  'smax_highlight_groups',
  'smax_specialists',
  'smax_feature_prefs',
  'smax_detractors',
  'smax_automatizadores',
  'smax_specialist_finals',
  'smax_specialist_tags',
  'smax_specialist_tag_keywords',
  'smax_specialist_highlight_terms'
];

function qid(name) {
  if (!/^[a-z_][a-z0-9_]*$/i.test(name)) throw new Error(`Identificador invalido: ${name}`);
  return `"${name.replace(/"/g, '""')}"`;
}

async function findLatestSnapshotDir() {
  const entries = await fs.readdir(EXPORT_BASE_DIR, { withFileTypes: true });
  const dirs = entries
    .filter((e) => e.isDirectory() && e.name.startsWith('snapshot-'))
    .map((e) => e.name)
    .sort();

  if (!dirs.length) {
    throw new Error(`Nenhum snapshot encontrado em ${EXPORT_BASE_DIR}`);
  }

  return path.join(EXPORT_BASE_DIR, dirs[dirs.length - 1]);
}

async function applyMigrations(client) {
  await client.query(`
    create table if not exists public.smax_automatizadores (
      id bigint generated by default as identity primary key,
      created_at timestamptz not null default now(),
      matricula integer not null,
      nome text not null default '',
      lotacao text not null default ''
    )
  `);

  const files = (await fs.readdir(MIGRATIONS_DIR))
    .filter((f) => f.toLowerCase().endsWith('.sql'))
    .sort();

  for (const file of files) {
    const full = path.join(MIGRATIONS_DIR, file);
    const sqlRaw = await fs.readFile(full, 'utf8');
    const sql = sqlRaw.replace(/^\uFEFF/, '');
    process.stdout.write(`Aplicando migration: ${file} ... `);
    await client.query(sql);
    process.stdout.write('ok\n');
  }
}

async function tableExists(client, table) {
  const r = await client.query(
    `select 1 from information_schema.tables where table_schema = 'public' and table_name = $1`,
    [table]
  );
  return r.rowCount > 0;
}

async function resetTables(client, tables) {
  if (!tables.length) return;
  const list = tables.map((t) => `public.${qid(t)}`).join(', ');
  await client.query(`truncate table ${list} restart identity cascade`);
}

async function insertJsonRows(client, table, rows) {
  if (!rows.length) return;
  const sql = `
    insert into public.${qid(table)}
    select *
    from jsonb_populate_recordset(null::public.${qid(table)}, $1::jsonb)
  `;
  await client.query(sql, [JSON.stringify(rows)]);
}

async function resetIdSequence(client, table) {
  const hasId = await client.query(
    `select 1 from information_schema.columns where table_schema = 'public' and table_name = $1 and column_name = 'id'`,
    [table]
  );
  if (hasId.rowCount === 0) return;

  const seq = await client.query(`select pg_get_serial_sequence($1, 'id') as seq`, [`public.${table}`]);
  const seqName = seq.rows[0] && seq.rows[0].seq;
  if (!seqName) return;

  const max = await client.query(`select max(id)::bigint as max_id from public.${qid(table)}`);
  const maxId = max.rows[0] && max.rows[0].max_id;
  if (maxId == null) return;

  await client.query(`select setval($1::regclass, $2::bigint, true)`, [seqName, maxId]);
}

async function loadSnapshot(snapshotDir) {
  const manifestPath = path.join(snapshotDir, 'manifest.json');
  const raw = await fs.readFile(manifestPath, 'utf8');
  const manifest = JSON.parse(raw);

  const exported = Object.entries(manifest.content || {})
    .filter(([, v]) => v && v.exported && v.file)
    .reduce((acc, [table, v]) => {
      acc[table] = path.join(snapshotDir, v.file);
      return acc;
    }, {});

  return { manifest, exported };
}

async function main() {
  const snapshotDir = process.env.SNAPSHOT_DIR || await findLatestSnapshotDir();
  const applyMigrationsEnabled = String(process.env.APPLY_MIGRATIONS || 'false').toLowerCase() === 'true';
  const dbConfig = {
    host: process.env.TARGET_DB_HOST || '187.77.232.228',
    port: Number(process.env.TARGET_DB_PORT || 5433),
    user: process.env.TARGET_DB_USER || 'admin',
    password: process.env.TARGET_DB_PASSWORD || 'postmax',
    database: process.env.TARGET_DB_NAME || 'sgs-smax',
    ssl: false
  };

  const { manifest, exported } = await loadSnapshot(snapshotDir);

  const client = new Client(dbConfig);
  await client.connect();

  try {
    const info = await client.query('select current_database() as db, current_user as usr, now() as now');
    console.log('Destino:', info.rows[0]);
    console.log('Snapshot:', snapshotDir);

    if (applyMigrationsEnabled) {
      await applyMigrations(client);
    } else {
      console.log('Aplicacao de migrations: pulada (APPLY_MIGRATIONS != true)');
    }

    const tablesToImport = [];
    for (const table of IMPORT_ORDER) {
      if (!(table in exported)) continue;
      if (!(await tableExists(client, table))) continue;
      tablesToImport.push(table);
    }

    console.log('Tabelas para importacao:', tablesToImport.join(', '));
    await resetTables(client, tablesToImport);

    const summary = [];
    for (const table of tablesToImport) {
      const file = exported[table];
      const rows = JSON.parse(await fs.readFile(file, 'utf8'));

      process.stdout.write(`Importando ${table} (${rows.length} rows) ... `);
      await insertJsonRows(client, table, rows);
      await resetIdSequence(client, table);
      const count = await client.query(`select count(*)::int as c from public.${qid(table)}`);
      const imported = Number(count.rows[0].c || 0);
      process.stdout.write(`ok (${imported})\n`);

      summary.push({
        table,
        sourceRows: rows.length,
        targetRows: imported,
        match: rows.length === imported
      });
    }

    const out = {
      snapshotDir,
      source: manifest.source,
      finishedAt: new Date().toISOString(),
      summary
    };

    const reportPath = path.join(snapshotDir, 'import-report-target.json');
    await fs.writeFile(reportPath, JSON.stringify(out, null, 2), 'utf8');

    console.log('Importacao concluida. Relatorio:', reportPath);
    for (const row of summary) {
      console.log(`${row.table}: source=${row.sourceRows}, target=${row.targetRows}, match=${row.match}`);
    }
  } finally {
    await client.end();
  }
}

main().catch((err) => {
  console.error('Falha no processo:', err);
  process.exit(1);
});
